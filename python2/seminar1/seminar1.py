from random import randint
LOWER_LIMIT = 0
UPPER_LIMIT = 1000
ATTEMPTS = 10
num = randint(LOWER_LIMIT, UPPER_LIMIT)
print(f'Загадано число от {LOWER_LIMIT} до {UPPER_LIMIT}')
print('Попробуйте отгадать число с 10 попыток')

attempt = 1
while attempt <= ATTEMPTS:
    user_try = int(input(f'Попытка {attempt}, Попробуйте угадать загаданное число: '))
    attempt += 1
    if user_try == num:
        result = 'Число {num} угадано!'
        break
    if num > user_try:
        print('Загаданное число больше введенного')
    else:
        print('Загаданное число меньше введенного')
else:
    result = 'Число {num} не угадано!'

print(result, '\n----------------------------------\n')
