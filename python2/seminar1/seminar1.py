print('Программа проверки простоты числа')
LOWER_LIMIT = 0
UPPER_LIMIT = 100000
ONE = 1
NULL = 0
result = 'Число простое'
while True:
    num = int(input('Введите число от 1 до 100000, 0 - выход: '))
    if num < LOWER_LIMIT or num > UPPER_LIMIT:
        continue
    break
if num == LOWER_LIMIT:
    result = 'Выход без проверки'
else:
    for i in range(LOWER_LIMIT + ONE + ONE, num):
        if num % i == NULL:
            result = 'Число составное'
            break

print(result, '\n----------------------------------\n')

